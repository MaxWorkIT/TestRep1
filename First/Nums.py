def plus(a, b):
    return a + b


def minus(a, b):
    return a - b


def mul(a, b):
    return a * b


def div(a, b):
    return a / b


def mod(a, b):
    return a % b


def floordiv(a, b):
    return a // b


if __name__ == '__main__':
    print(plus(2, 2))
    print(minus(4, 2))
    print(mul(3, 3))
    print(div(6, 2))
    print(mod(7, 4))
    print(floordiv(9, 4))











